const mongoose = require("mongoose");

const forgetPasswordOTP = new mongoose.Schema({
  email: {
    type: String,
    required: true,
  },
  otp: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    required: true,
  },
  expiresAt: {
    type: Date,
    required: true,
  },
});

const OTP = mongoose.model("otp", forgetPasswordOTP);

module.exports = OTP;
